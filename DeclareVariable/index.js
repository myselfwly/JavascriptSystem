/**
 * javascript 总定义变量有3种方式
 * 1. var
 * 2. let
 * 3. const
 */

/**
 * var 关键字
 */
var n = 1
var o = {}
var s = 'string'
var n = 2
var b = false

/**
 * let 关键字
 */
let ln = 1
let lo = {}
let ls = 'string'
let b = true

/**
 * const 关键字
 */
const cn = 1
const co = {}
const cs = 'string'
const cb = false

